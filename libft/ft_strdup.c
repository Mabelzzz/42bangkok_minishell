#include "libft.h"

char	*ft_strdup(char *s1)
{
	char	*dest;
	int		len;
	int		i;

	len = ft_strlen(s1);
	dest = (char *)malloc(sizeof(char) * len + 1);
	if (!dest)
		return (0);
	i = 0;
	while (i < len)
	{
		dest[i] = s1[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
